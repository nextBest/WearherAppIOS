import UIKit

extension UIImage {
    class var bgNight: UIImage {
        return UIImage(named: "bg_night")!
    }
    
    class var bgDay: UIImage {
        return UIImage(named: "bg_day")!
    }
    
    class var sky: UIImage {
        return UIImage(named: "sky")!
    }
}
